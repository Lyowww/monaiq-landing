export function getAIFinanceContent(locale: AIFinanceLocale) {
  return locale === "en" ? en : hy;
}

export function getMoneyPageJsonLd(args: {
  locale: AIFinanceLocale;
  pageUrl: string;
  description: string;
}) {
  return {
    "@context": "https://schema.org",
    "@type": "SoftwareApplication",
    "@id": `${args.pageUrl}#software`,
    name: "MonAIQ",
    applicationCategory: "FinanceApplication",
    operatingSystem: "iOS, Android",
    offers: { "@type": "Offer", price: "0", priceCurrency: "USD" },
    url: args.pageUrl,
    downloadUrl: args.pageUrl,
    description: args.description,
    inLanguage: args.locale === "hy" ? "hy" : "en",
  };
}

export function getMoneyPageWebPageJsonLd(args: {
  pageUrl: string;
  name: string;
  description: string;
  siteOrigin: string;
}) {
  return {
    "@context": "https://schema.org",
    "@type": "WebPage",
    "@id": `${args.pageUrl}#webpage`,
    url: args.pageUrl,
    name: args.name,
    description: args.description,
    isPartOf: { "@type": "WebSite", name: "MonAIQ", url: args.siteOrigin },
  };
}
